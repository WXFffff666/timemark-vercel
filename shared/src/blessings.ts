// 获取祝福语 - 智能匹配逻辑
// 1. 自定义祝福语（用户为事件单独设置）→ 直接返回
// 2. 自己过自己的生日（被提醒人==提醒人）→ 用通用祝福
// 3. 提醒他人（被提醒人!=提醒人）→ 根据事件名称匹配被提醒人
// 4. 没有匹配 → 用通用模板
export function getBlessing(
  type: string, 
  customMessage?: string, 
  personName?: string,      // 被提醒人/事件所有者（妈妈）
  reminderRecipientName?: string  // 提醒人/接收通知的人（我）
): string {
  // 1. 优先使用用户自定义的祝福语
  if (customMessage && customMessage.trim()) {
    return customMessage.trim();
  }
  
  // 2. 智能匹配：根据被提醒人和提醒人关系
  const eventName = personName?.trim() || '';
  const recipientName = reminderRecipientName?.trim() || '';
  
  // 自己过自己的生日（被提醒人 == 提醒人，或者没填提醒人）
  if (eventName && (eventName === recipientName || !recipientName)) {
    // 用通用祝福（适用于自己过生日的情况）
    const defaultList = personBlessingMap[type]?.['default'] || blessings[type as keyof typeof blessings] || blessings.other;
    return defaultList[Math.floor(Math.random() * defaultList.length)];
  }
  
  // 3. 提醒他人 → 根据事件名称/被提醒人匹配祝福语
  if (eventName) {
    const map = personBlessingMap[type] || {};
    
    // 精确匹配（事件名称包含关键词）
    for (const [keyword, blessingList] of Object.entries(map)) {
      if (keyword !== 'default' && eventName.includes(keyword)) {
        return blessingList[Math.floor(Math.random() * blessingList.length)];
      }
    }
  }
  
  // 4. 没有匹配到 → 用通用默认祝福
  const defaultList = personBlessingMap[type]?.['default'] || blessings[type as keyof typeof blessings] || blessings.other;
  return defaultList[Math.floor(Math.random() * defaultList.length)];
}
